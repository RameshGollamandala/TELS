CREATE OR REPLACE FORCE VIEW TELS_RCRM_ATTRIBUTES_VIEW (
    FILENAME,
    BATCHPROCESSDATE,
    RECORDNUMBER,
    IS_FILTERED,
    ORDER_NUMBER,
    ORDER_SUB_TYPE,
    STATUS_HEADER,
    ROW_ID,
    ATTRIBUTE_ROW_ID,
    ATTRIBUTE_ACTION_CODE,
    ATTRIBUTE_DISPLAY_NAME,
    ATTRIBUTE_VALUE
) AS
    SELECT
        FILENAME,
        BATCHPROCESSDATE,
        RECORDNUMBER,
        IS_FILTERED,
        ORDER_NUMBER,
        ORDER_SUB_TYPE,
        STATUS_HEADER,
        ROW_ID,
        ATTRIBUTE_ROW_ID,
        ATTRIBUTE_ACTION_CODE,
        ATTRIBUTE_DISPLAY_NAME,
        ATTRIBUTE_VALUE
    FROM
        TELS_RCRM_MV_ATTRIBUTE;